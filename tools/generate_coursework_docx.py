def build_document(body: ET.Element, sect_pr: ET.Element) -> None:
    build_title_and_toc(body)
    for text, style, align, indent in build_main_content():
        body.append(make_paragraph(text, style=style, align=align, first_line=indent))
    body.append(copy.deepcopy(sect_pr))


def main() -> None:
    if not TEMPLATE.exists():
        raise FileNotFoundError(f"Template not found: {TEMPLATE}")

    with zipfile.ZipFile(TEMPLATE, "r") as src:
        file_data = {name: src.read(name) for name in src.namelist()}

    document_root = ET.fromstring(file_data["word/document.xml"])
    body = document_root.find(w("body"))
    if body is None:
        raise RuntimeError("word/document.xml does not contain body")

    sect_pr = body.find(w("sectPr"))
    if sect_pr is None:
        raise RuntimeError("Template body does not contain sectPr")

    sect_pr_copy = copy.deepcopy(sect_pr)
    for child in list(body):
        body.remove(child)

    build_document(body, sect_pr_copy)
    file_data["word/document.xml"] = ET.tostring(document_root, encoding="utf-8", xml_declaration=True)

    settings_root = ET.fromstring(file_data["word/settings.xml"])
    ensure_update_fields(settings_root)
    file_data["word/settings.xml"] = ET.tostring(settings_root, encoding="utf-8", xml_declaration=True)

    with zipfile.ZipFile(OUTPUT, "w", compression=zipfile.ZIP_DEFLATED) as dst:
        for name, data in file_data.items():
            dst.writestr(name, data)

    print(f"CREATED: {OUTPUT}")


if __name__ == "__main__":
    main()
