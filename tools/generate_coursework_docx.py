def make_paragraph(
    text: str = "",
    *,
    style: str = "a",
    align: str | None = None,
    first_line: int | None = None,
    bold: bool = False,
) -> ET.Element:
    p = ET.Element(w("p"))
    p_pr = ET.SubElement(p, w("pPr"))
    ET.SubElement(p_pr, w("pStyle"), {w("val"): style})
    if align:
        ET.SubElement(p_pr, w("jc"), {w("val"): align})
    if first_line is not None:
        ET.SubElement(p_pr, w("ind"), {w("firstLine"): str(first_line)})
    if text:
        r = ET.SubElement(p, w("r"))
        if bold:
            r_pr = ET.SubElement(r, w("rPr"))
            ET.SubElement(r_pr, w("b"))
        t = ET.SubElement(r, w("t"))
        if text.startswith(" ") or text.endswith(" "):
            t.set(f"{{{XML_NS}}}space", "preserve")
        t.text = text
    return p


def make_blank_paragraph() -> ET.Element:
    return make_paragraph("")


def make_page_break_paragraph() -> ET.Element:
    p = ET.Element(w("p"))
    r = ET.SubElement(p, w("r"))
    ET.SubElement(r, w("br"), {w("type"): "page"})
    return p


def make_toc_paragraph() -> ET.Element:
    p = ET.Element(w("p"))
    p_pr = ET.SubElement(p, w("pPr"))
    ET.SubElement(p_pr, w("ind"), {w("firstLine"): "0"})
    r1 = ET.SubElement(p, w("r"))
    ET.SubElement(r1, w("fldChar"), {w("fldCharType"): "begin"})
    r2 = ET.SubElement(p, w("r"))
    instr = ET.SubElement(r2, w("instrText"))
    instr.set(f"{{{XML_NS}}}space", "preserve")
    instr.text = ' TOC \\o "1-3" \\h \\z \\u '
    r3 = ET.SubElement(p, w("r"))
    ET.SubElement(r3, w("fldChar"), {w("fldCharType"): "separate"})
    r4 = ET.SubElement(p, w("r"))
    ET.SubElement(r4, w("t")).text = "Оглавление будет обновлено при открытии документа."
    r5 = ET.SubElement(p, w("r"))
    ET.SubElement(r5, w("fldChar"), {w("fldCharType"): "end"})
    return p


def add_paragraphs(body: ET.Element, paragraphs: list[ET.Element]) -> None:
    for paragraph in paragraphs:
        body.append(paragraph)


def ensure_update_fields(settings_root: ET.Element) -> None:
    node = settings_root.find(w("updateFields"))
    if node is None:
        node = ET.SubElement(settings_root, w("updateFields"))
    node.set(w("val"), "true")


