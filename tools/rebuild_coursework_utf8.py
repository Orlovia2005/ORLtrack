def ensure_style_size(styles_root):
    for style_id in ['a', '1', '2', '3']:
        style = styles_root.find(f".//{w('style')}[@{w('styleId')}='{style_id}']")
        if style is None:
            continue
        rpr = style.find(w('rPr'))
        if rpr is None:
            rpr = ET.SubElement(style, w('rPr'))
        rfonts = rpr.find(w('rFonts'))
        if rfonts is None:
            rfonts = ET.SubElement(rpr, w('rFonts'))
        rfonts.set(w('ascii'), 'Times New Roman')
        rfonts.set(w('hAnsi'), 'Times New Roman')
        rfonts.set(w('cs'), 'Times New Roman')
        sz = rpr.find(w('sz'))
        if sz is None:
            sz = ET.SubElement(rpr, w('sz'))
        sz.set(w('val'), '28')
        sz_cs = rpr.find(w('szCs'))
        if sz_cs is None:
            sz_cs = ET.SubElement(rpr, w('szCs'))
        sz_cs.set(w('val'), '28')

