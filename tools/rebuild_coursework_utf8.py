def add_image_relationship(rels_root, file_data, image_path: Path):
    used_ids = []
    for node in rels_root.findall(rel('Relationship')):
        rid = node.attrib.get('Id', '')
        if rid.startswith('rId'):
            try:
                used_ids.append(int(rid[3:]))
            except ValueError:
                pass
    next_id = max(used_ids or [0]) + 1
    rid = f'rId{next_id}'
    target = f'media/{image_path.name}'
    ET.SubElement(
        rels_root,
        rel('Relationship'),
        {
            'Id': rid,
            'Type': 'http://schemas.openxmlformats.org/officeDocument/2006/relationships/image',
            'Target': target,
        },
    )
    file_data[f'word/{target}'] = image_path.read_bytes()
    return rid


def image_para(rel_id: str, name: str, docpr_id: int, cx: int = 5486400, cy: int = 3086100):
    p = ET.Element(w('p'))
    ppr = ET.SubElement(p, w('pPr'))
    ET.SubElement(ppr, w('jc'), {w('val'): 'center'})
    ET.SubElement(ppr, w('ind'), {w('firstLine'): '0'})
    r_node = ET.SubElement(p, w('r'))
    rpr = ET.SubElement(r_node, w('rPr'))
    ET.SubElement(rpr, w('noProof'))
    drawing = ET.SubElement(r_node, w('drawing'))
    inline = ET.SubElement(drawing, wp('inline'), {'distT': '0', 'distB': '0', 'distL': '0', 'distR': '0'})
    ET.SubElement(inline, wp('extent'), {'cx': str(cx), 'cy': str(cy)})
    ET.SubElement(inline, wp('effectExtent'), {'l': '0', 't': '0', 'r': '0', 'b': '0'})
    ET.SubElement(inline, wp('docPr'), {'id': str(docpr_id), 'name': name})
    c_nv = ET.SubElement(inline, wp('cNvGraphicFramePr'))
    ET.SubElement(c_nv, a('graphicFrameLocks'), {'noChangeAspect': '1'})
    graphic = ET.SubElement(inline, a('graphic'))
    graphic_data = ET.SubElement(graphic, a('graphicData'), {'uri': 'http://schemas.openxmlformats.org/drawingml/2006/picture'})
    pic_node = ET.SubElement(graphic_data, pic('pic'))
    nv_pic_pr = ET.SubElement(pic_node, pic('nvPicPr'))
    ET.SubElement(nv_pic_pr, pic('cNvPr'), {'id': str(docpr_id), 'name': name})
    ET.SubElement(nv_pic_pr, pic('cNvPicPr'))
    blip_fill = ET.SubElement(pic_node, pic('blipFill'))
    ET.SubElement(blip_fill, a('blip'), {r('embed'): rel_id})
    stretch = ET.SubElement(blip_fill, a('stretch'))
    ET.SubElement(stretch, a('fillRect'))
    sp_pr = ET.SubElement(pic_node, pic('spPr'))
    xfrm = ET.SubElement(sp_pr, a('xfrm'))
    ET.SubElement(xfrm, a('off'), {'x': '0', 'y': '0'})
    ET.SubElement(xfrm, a('ext'), {'cx': str(cx), 'cy': str(cy)})
    prst = ET.SubElement(sp_pr, a('prstGeom'), {'prst': 'rect'})
    ET.SubElement(prst, a('avLst'))
    return p


def page_break():
    p = ET.Element(w('p'))
    r = ET.SubElement(p, w('r'))
    ET.SubElement(r, w('br'), {w('type'): 'page'})
    return p


