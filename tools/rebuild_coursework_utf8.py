def para(text='', style='a', align=None, indent=708, bold=False, font_name='Times New Roman', size_half_points=28):
    p = ET.Element(w('p'))
    ppr = ET.SubElement(p, w('pPr'))
    ET.SubElement(ppr, w('pStyle'), {w('val'): style})
    if align:
        ET.SubElement(ppr, w('jc'), {w('val'): align})
    ET.SubElement(ppr, w('ind'), {w('firstLine'): str(indent)})
    if text:
        r = ET.SubElement(p, w('r'))
        rpr = ET.SubElement(r, w('rPr'))
        ET.SubElement(rpr, w('rFonts'), {w('ascii'): font_name, w('hAnsi'): font_name, w('cs'): font_name})
        ET.SubElement(rpr, w('sz'), {w('val'): str(size_half_points)})
        ET.SubElement(rpr, w('szCs'), {w('val'): str(size_half_points)})
        if bold:
            ET.SubElement(rpr, w('b'))
        t = ET.SubElement(r, w('t'))
        if text.startswith(' ') or text.endswith(' '):
            t.set(f'{{{XML_NS}}}space', 'preserve')
        t.text = text
    return p


